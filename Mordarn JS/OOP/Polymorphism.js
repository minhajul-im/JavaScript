/**
 * What is Polymorphism?
 * 
  Polymorphism is a concept in object-oriented programming that allows objects of different classes to be treated as objects of a common parent class. It enables a single interface to be used to represent multiple related classes.

  In polymorphism, objects can be passed or accessed through a common interface, such as a shared method or property, even though they may have different implementations for that interface.
 */
