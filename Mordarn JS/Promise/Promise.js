/**
 * What is promise?
 * The Promise is an object that represents the eventual completion (or failure) of an asynchronous operation and its resulting value.
 *
 * A promise has three states (pending, fulfilled, and rejected.)
 *
 * When a promise is first created, it is in the pending state. When the asynchronous operation completes successfully, the promise is fulfilled. When the asynchronous operation fails, the promise is rejected.
 */
