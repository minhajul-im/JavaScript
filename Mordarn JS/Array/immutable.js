// all immutable methods

concat()`: Returns a new array that combines the elements of the original array with other arrays or values.`;

slice()`: Returns a shallow copy of a portion of an array into a new array.`;

forEach()`: Executes a provided function once for each array element.`;

map()`: Creates a new array by calling a provided function on each element in the array.`;

filter()`: Creates a new array with all elements that pass the test implemented by the provided function.`;

find()`: Returns the first element in the array that satisfies the provided testing function.`;

findIndex()`: Returns the index of the first element in the array that satisfies the provided testing function.`;

every()`: Checks if all elements in the array pass a provided testing function.`;

some()`: Checks if at least one element in the array passes a provided testing function.`;

reduce()`: Applies a function to reduce the array to a single value.`;

reduceRight()`: Applies a function to reduce the array from right to left to a single value.`;

includes()`: Determines whether an array includes a certain value.`;

indexOf()`: Returns the first index at which a given element can be found in the array.`;

lastIndexOf()`: Returns the last index at which a given element can be found in the array.`;

join()`: Joins all elements of an array into a string.`;
